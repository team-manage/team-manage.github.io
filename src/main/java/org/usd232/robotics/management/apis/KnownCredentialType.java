package org.usd232.robotics.management.apis;

/**
 * The type of credential the user still knows about his/her account
 * 
 * @author Zach Deibert
 * @since 1.0
 * @version 1.0
 */
public enum KnownCredentialType
{
    /**
     * The user knows his/her username
     * 
     * @since 1.0
     */
    username,
    /**
     * The user knows his/her email
     * 
     * @since 1.0
     */
    email,
    /**
     * The user knows his/her phone number
     * 
     * @since 1.0
     */
    phone
}
