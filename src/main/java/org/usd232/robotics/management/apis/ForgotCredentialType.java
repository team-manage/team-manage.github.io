package org.usd232.robotics.management.apis;

/**
 * The type of credential the user has forgotten about his/her account
 * 
 * @author Zach Deibert
 * @since 1.0
 * @version 1.0
 */
public enum ForgotCredentialType
{
    /**
     * The user has forgotten his/her username
     * 
     * @since 1.0
     */
    username,
    /**
     * The user has forgotten his/her password
     * 
     * @since 1.0
     */
    password
}
