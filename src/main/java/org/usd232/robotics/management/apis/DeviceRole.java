package org.usd232.robotics.management.apis;

/**
 * The role of a device running a server
 * 
 * @author Zach Deibert
 * @since 1.0
 * @version 1.0
 */
public enum DeviceRole
{
    /**
     * The device is a web server
     * 
     * @since 1.0
     */
    server,
    /**
     * The device is an access point for automatic sign in
     * 
     * @since 1.0
     */
    AP
}
