package org.usd232.robotics.management.apis;

/**
 * The type of a setting
 * 
 * @author Zach Deibert
 * @since 1.0
 * @version 1.0
 */
public enum SettingType
{
    /**
     * The setting is a String
     * 
     * @since 1.0
     */
    string,
    /**
     * The setting is an int
     * 
     * @since 1.0
     */
    integer,
    /**
     * The setting is a double
     * 
     * @since 1.0
     */
    number
}
